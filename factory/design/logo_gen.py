async def send_brand_assets_to_telegram(
    operator_id: str,
    assets: dict,
    app_name: str,
) -> None:
    """Send generated logo and splash screen to the operator via Telegram.

    Silently skips assets that are None.
    """
    from factory.telegram.notifications import send_telegram_message

    if not assets.get("logo_bytes") and not assets.get("splash_bytes"):
        await send_telegram_message(
            operator_id,
            f"Design assets for {app_name} could not be generated. "
            "The pipeline will continue with placeholder assets."
        )
        return

    try:
        from factory.app import _get_bot_app
        bot_app = _get_bot_app()
        bot = bot_app.bot if bot_app else None
    except Exception:
        bot = None

    if not bot:
        logger.warning("Bot instance unavailable — skipping asset Telegram delivery")
        return

    import io
    caption_logo = (
        f"🎨 *{app_name}* — App Icon\n"
        "Generated by the AI design pipeline. You can request changes later."
    )
    caption_splash = f"🖼 *{app_name}* — Splash Screen"

    if assets.get("logo_bytes"):
        try:
            await bot.send_photo(
                chat_id=int(operator_id),
                photo=io.BytesIO(assets["logo_bytes"]),
                caption=caption_logo,
                parse_mode="Markdown",
            )
        except Exception as e:
            logger.warning(f"Failed to send logo to Telegram: {e}")

    if assets.get("splash_bytes"):
        try:
            await bot.send_photo(
                chat_id=int(operator_id),
                photo=io.BytesIO(assets["splash_bytes"]),
                caption=caption_splash,
                parse_mode="Markdown",
            )
        except Exception as e:
            logger.warning(f"Failed to send splash to Telegram: {e}")


