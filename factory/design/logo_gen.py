"""
AI Factory Pipeline v5.8 — App Logo & Design Asset Generator

Generates logos, splash screens, and icon sets for apps using the
free image generation chain (Pollinations → HuggingFace → Together).

Usage in pipeline (S2 Blueprint):
    from factory.design.logo_gen import generate_brand_assets
    assets = await generate_brand_assets(state, blueprint_data)
    # assets["logo_bytes"], assets["splash_bytes"], assets["logo_path"]

Spec Authority: v5.8 §4.3.1 (Design Asset Generation)
"""

from __future__ import annotations

import logging
import os
from typing import Optional

from factory.integrations.image_gen import (
    generate_image,
    build_logo_prompt,
    build_splash_prompt,
)

logger = logging.getLogger("factory.design.logo_gen")


