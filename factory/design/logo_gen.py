async def generate_brand_assets(
    state,
    blueprint_data: dict,
) -> dict:
    """Generate logo + splash screen for the app.

    Args:
        state: PipelineState (for project_id, idea_name, s0_output)
        blueprint_data: S2 blueprint dict (color_palette, app_name, etc.)

    Returns:
        Dict with keys:
            logo_bytes: PNG bytes or None
            splash_bytes: PNG bytes or None
            logo_path: local file path if saved, else None
            splash_path: local file path if saved, else None
            logo_prompt: the prompt used
    """
    project_id = state.project_id
    app_name = (
        blueprint_data.get("app_name")
        or state.idea_name
        or (state.s0_output or {}).get("app_name")
        or "App"
    )
    description = (state.s0_output or {}).get("app_description", "")
    colors = blueprint_data.get("color_palette", {})
    primary = colors.get("primary", "#6366f1")
    secondary = colors.get("secondary", "#8b5cf6")

    results: dict = {
        "logo_bytes": None,
        "splash_bytes": None,
        "logo_path": None,
        "splash_path": None,
        "logo_prompt": "",
    }

    # ── Logo ──
    logo_prompt = build_logo_prompt(app_name, description, primary)
    results["logo_prompt"] = logo_prompt
    logger.info(f"[{project_id}] Generating logo for '{app_name}'")

    logo_bytes = await generate_image(
        prompt=logo_prompt,
        width=1024,
        height=1024,
        style="flat app icon",
    )

    if logo_bytes:
        results["logo_bytes"] = logo_bytes
        path = _save_asset(logo_bytes, state, "logo.png")
        results["logo_path"] = path
        logger.info(f"[{project_id}] Logo generated ({len(logo_bytes)} bytes)")
    else:
        logger.warning(f"[{project_id}] Logo generation failed — all providers exhausted")

    # ── Splash Screen ──
    splash_prompt = build_splash_prompt(app_name, description, primary, secondary)
    logger.info(f"[{project_id}] Generating splash screen")

    splash_bytes = await generate_image(
        prompt=splash_prompt,
        width=1080,
        height=1920,
        style="mobile app splash screen",
    )

    if splash_bytes:
        results["splash_bytes"] = splash_bytes
        path = _save_asset(splash_bytes, state, "splash.png")
        results["splash_path"] = path
        logger.info(f"[{project_id}] Splash generated ({len(splash_bytes)} bytes)")
    else:
        logger.warning(f"[{project_id}] Splash generation failed")

    return results


